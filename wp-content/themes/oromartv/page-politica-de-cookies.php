La presente Política se aplica a los siguientes sitios web de PERONE S.A.: oromartv.com, a las aplicaciones móviles y aplicaciones para televisiones conectadas (SmartTV).

Al acceder al sitio web, aplicación móvil o aplicación para Smart TV, te mostraremos un aviso de cookies o tecnologías similares informando de cómo puedes prestar el consentimiento o rechazar o configurar el uso de cookies a través de un panel de configuración (en adelante, Panel de Configuración). Podrás asimismo acceder a dicho panel para modificar su configuración en cualquier momento a través del link “Configuración de Privacidad” en el pie de página.

El consentimiento para la instalación de cookies no es un requisito obligatorio para acceder al sitio web. No obstante, si decides no otorgarlo es posible que haya algunos elementos que no funcionen correctamente, o que recibas publicidad que no sea de tu interés.

La información obtenida a través de las cookies será asimismo tratada por las empresas colaboradoras con las finalidades indicadas en sus respectivas políticas de privacidad. Puedes conocer las empresas colaboradoras que incorporan sus cookies en nuestros sitios web, aplicaciones móviles y aplicaciones para SmartTV, qué datos recopilan y con qué finalidad, en la pantalla de configuración o a través de la sección “Configuración de Privacidad” del pie de la página. <br><br>

¿Qué son las cookies?<br><br>

Las cookies, identificadores publicitarios y/o tecnologías similares (en adelante “cookies”) son ficheros que se descargan en el dispositivo del usuario (ordenador, Smartphone, tablet, Smart tv, etc.) y que almacenan y recuperan información cuando navegas o accedes a los diferentes servicios. Las cookies permiten, entre otras cosas, reconocerte como usuario, obtener información sobre tus hábitos de navegación o personalizar la forma en que se muestra el contenido y la publicidad.

En esta Política se detallan las finalidades con las que PERONE S.A. utiliza la información obtenida a través de las cookies, entre otras: posibilitar o mejorar el servicio ofrecido, mostrar los contenidos y publicidad general y personalizada o realizar mediciones sobre el comportamiento del usuario en el sitio.

¿Qué datos personales tratamos a través del uso de cookies?<br><br>

A través de las cookies obtenemos los denominados datos de navegación. Estos datos incluyen la dirección IP desde la que te has conectado, cookies u otros dispositivos de identificación, identificadores publicitarios activados en el dispositivo en el caso de que hayas autorizado su uso, características del dispositivo como la marca, modelo, tamaño de la pantalla, sistema operativo y su versión, detalles del navegador usado, el tipo de red a la que estás conectado, identificadores del dispositivo y la identificación de los distintos contenidos que visitas.

Asimismo, obtenemos datos de publicidad, que son aquellos datos relativos a los anuncios que te mostramos y cómo interactúas con ellos, por ejemplo, si se ha hecho un clic sobre un anuncio, el tiempo que ha estado visible en la pantalla o la noticia en la que se ha mostrado el anuncio.

Tipos de Cookies <br><br>

Hay distintos tipos de cookies, según la entidad que las gestione, el plazo de tiempo que permanecen activadas y su finalidad.

·       Según la entidad que gestione el equipo o dominio desde dónde se envían y trate los datos que se obtengan:

o   Cookies propias: aquellas gestionadas por el propio editor y desde el que se presta el servicio solicitado.

o   Cookies de tercero: aquellas que se envían desde un equipo o dominio que no es gestionado por el editor, sino por otra entidad que trata los datos obtenidos a través de las cookies.

·       Según el plazo de tiempo que permanecen activadas en el dispositivo:

o   Cookies de sesión: recaban y almacenar datos mientras accedes a nuestros servicios, empleándose para almacenar información que sólo interesa conservar durante la sesión.

o   Cookies persistentes: los datos siguen almacenados en el terminal y pueden ser accedidos y tratados durante un periodo definido por el responsable de la cookie.

·       Según la finalidad para la que se traten los datos obtenidos:

o   Técnicas: son aquellas que permiten al usuario navegar por el sitio web o acceder y utilizar los contenidos y servicios que incluyen nuestras aplicaciones. Estas cookies se utilizan para operar el servicio, mostrar contenidos correctamente y permitir al usuario interactuar con las funcionalidades seleccionadas, incluyendo aquellas cookies que permiten gestionar la publicidad que se muestra en los distintos espacios publicitarios, sin recopilar información de los usuarios con fines distintos como crear un perfil del usuario o mostrarle contenido o publicidad personalizada.

o   Cookies de personalización: Hacen posible que cada usuario pueda configurar aspectos como el lenguaje en el que desea ver la página web o los servicios, formatos de visualización, etc.

o   Analíticas: son aquellas que, ya sean tratadas por nosotros o por terceros, nos permiten cuantificar el número de usuarios y así realizar la medición y análisis estadístico de la utilización que hacen los usuarios del servicio. Para ello se analiza su navegación en nuestras páginas web con el fin de mejorar la oferta de productos o servicios que ofrecemos.

o   Cookies de redes sociales: facilitan al usuario compartir contenidos en redes sociales de su interés. Mediante su utilización el usuario no tiene que introducir nuevamente sus datos de acceso para compartir contenidos.

o   Cookies de publicidad comportamental: son aquellas que, ya sean tratadas por nosotros o por terceros, permiten analizar el comportamiento del usuario durante el uso del servicio para mostrarle publicidad afín a sus intereses basada en sus preferencias y hábitos de navegación o visualización de contenidos. Con tu consentimiento, podemos utilizar bases de datos externas para la creación de estos perfiles publicitarios, de acuerdo con lo detallado en el Panel de Configuración y la Política de Privacidad.

A través del Panel de Configuración de Cookies puedes obtener la información actualizada de las cookies que hay en nuestros sitios web y aplicaciones, junto con información relativa al proveedor de cada una de ellas, el propósito o finalidad de la misma, así como su duración y la Política de privacidad del proveedor.

¿Cómo puedo desactivar las cookies que utiliza esta página web?<br> <br>

Si deseas permitir o desactivar las cookies, puedes hacerlo a través del Panel de Configuración accesible en el link “Configuración de Privacidad” que puedes encontrar en el pie de página de nuestras páginas web o en Ajustes>Ajustes de Privacidad de las aplicaciones.

Puedes también gestionar su desactivación, así como borrar tus datos de navegación, a través de la configuración de tu navegador o dispositivo. Consulta las opciones e instrucciones que te ofrece tu navegador. No obstante, en el caso de que decidas bloquearlas, es posible que ciertos servicios que necesiten su uso no estén disponibles.
Puedes llevar a cabo la gestión de las cookies en las siguientes ubicaciones de los navegadores más populares: <br><br>

·       Chrome, desde http://support.google.com/chrome/bin/answer.py?hl=es&answer=95647 <br>

·       Windows Edge, desde https://support.microsoft.com/es-es/help/4468242/microsoft-edge-browsing-data-and-privacy-microsoft-privacy <br>

·       Firefox, desde http://support.mozilla.org/es/kb/habilitar-y-deshabilitar-cookies-que-los-sitios-we <br>

·       Safari, desde http://support.apple.com/kb/ph5042 <br><br>

Ten en cuenta que, en cada caso, la desactivación para cada navegador y dispositivo es independiente. Si deseas desactivar las cookies en todos los navegadores y dispositivos deberás hacerlo en cada uno de ellos individualmente.

Además te informamos de que la desactivación no surte efectos de forma inmediata y que se hará efectiva una vez se actualice la información en los sistemas.

